// Scans public/providers for images and writes a slug -> path manifest that the
// app uses to auto-match a solution's service provider to its photo. Drop a file
// named after the provider (e.g. "Alner.webp", "Refill Co.jpg") into
// public/providers and it maps automatically. Runs on predev / prebuild.
import { readdirSync, writeFileSync, existsSync, mkdirSync } from "node:fs";
import { join, extname, basename, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");
const providersDir = join(root, "public", "providers");
const outFile = join(root, "src", "lib", "provider-images.generated.ts");

const IMAGE_EXTS = new Set([".webp", ".jpg", ".jpeg", ".png", ".avif", ".gif"]);

// Must stay in sync with slugifyName() in src/lib/utils.ts.
function slugifyName(name) {
  return (name ?? "")
    .toLowerCase()
    .normalize("NFKD")
    .replace(/[^a-z0-9]+/g, "");
}

if (!existsSync(providersDir)) {
  mkdirSync(providersDir, { recursive: true });
}

const files = existsSync(providersDir)
  ? readdirSync(providersDir).filter((f) => IMAGE_EXTS.has(extname(f).toLowerCase()))
  : [];

const map = {};
const collisions = [];
for (const file of files.sort()) {
  const key = slugifyName(basename(file, extname(file)));
  if (!key) continue;
  if (map[key] && map[key] !== `/providers/${file}`) {
    collisions.push(`${file} collides with ${map[key]}`);
  }
  map[key] = `/providers/${file}`;
}

const body = Object.entries(map)
  .map(([k, v]) => `  ${JSON.stringify(k)}: ${JSON.stringify(v)},`)
  .join("\n");

const contents = `// AUTO-GENERATED by scripts/generate-provider-images.mjs. Do not edit by hand.
// Maps a slugified service-provider name to its image in /public/providers.
export const PROVIDER_IMAGES: Record<string, string> = {
${body}
};
`;

writeFileSync(outFile, contents, "utf8");
console.log(`[provider-images] mapped ${Object.keys(map).length} image(s) -> ${outFile}`);
if (collisions.length) {
  console.warn(`[provider-images] name collisions:\n  ${collisions.join("\n  ")}`);
}
