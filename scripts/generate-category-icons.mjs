import { readFileSync, writeFileSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");
const iconsDir = join(root, "public", "icons", "categories");
const outFile = join(root, "src", "lib", "category-icon-svgs.generated.ts");

/** Main category icons + optional matrix axis/header icons. */
const ICON_FILES = {
  "Packaging Reuse": "packaging-reuse.svg",
  Refill: "refill.svg",
  "Product Reuse": "product-reuse.svg",
  "Use of Reusable Product Alternatives": "reusable-product-alternatives.svg",
  "Transfer-based Reuse": "transfer-based-reuse.svg",
  "System-Owned": "system-owned.svg",
  "User-Owned": "user-owned.svg",
  "Transfer of Ownership": "transfer-of-ownership.svg",
  Packaging: "packaging.svg",
  Product: "product.svg",
};

function extractInner(svg) {
  const gMatch = svg.match(/<g[^>]*>([\s\S]*?)<\/g>/);
  if (gMatch) return gMatch[1].trim();

  const withoutComments = svg.replace(/<!--[\s\S]*?-->/g, "");
  const innerMatch = withoutComments.match(/<svg[^>]*>([\s\S]*?)<\/svg>/i);
  if (!innerMatch) return "";
  return innerMatch[1].trim();
}

function extractStrokeWidth(svg) {
  const gMatch = svg.match(/<g[^>]*stroke-width="([^"]+)"/);
  if (gMatch) return gMatch[1];
  const svgMatch = svg.match(/<svg[^>]*stroke-width="([^"]+)"/);
  if (svgMatch) return svgMatch[1];
  return "1.8";
}

const entries = Object.entries(ICON_FILES).map(([label, file]) => {
  const svg = readFileSync(join(iconsDir, file), "utf8");
  const inner = extractInner(svg);
  const strokeWidth = extractStrokeWidth(svg);
  return { label, inner, strokeWidth };
});

const body = entries
  .map(
    ({ label, inner, strokeWidth }) =>
      `  ${JSON.stringify(label)}: { inner: ${JSON.stringify(inner)}, strokeWidth: ${JSON.stringify(strokeWidth)} },`
  )
  .join("\n");

const contents = `// AUTO-GENERATED by scripts/generate-category-icons.mjs. Do not edit by hand.
// Inner SVG markup from public/icons/categories/*.svg (uses currentColor via parent stroke).

export type CategoryIconData = { inner: string; strokeWidth: string };

export const CATEGORY_ICON_SVGS: Record<string, CategoryIconData> = {
${body}
};
`;

writeFileSync(outFile, contents, "utf8");
console.log(`[category-icons] wrote ${Object.keys(ICON_FILES).length} icon(s) -> ${outFile}`);
